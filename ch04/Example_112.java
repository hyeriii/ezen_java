package ch04;

public class Example_112 {

	public static void main(String[] args) {
		// 112P if-else if-else

		int score = 75;
		
		if(score>=90) {
			System.out.println("등급은 A입니다");
		}else if(score>=80) {
			System.out.println("등급은 B입니다");
		}else if(score>=70) {
			System.out.println("등급은 C입니다");
		}else{
			System.out.println("등급은 D입니다");
		}
		
	}

}
